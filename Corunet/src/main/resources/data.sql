/* ELIMINAR LOS DATOS QUE EXISTEN EN LA BBDD */
DROP TABLE IF EXISTS PRICES;
DROP TABLE IF EXISTS BRAND;

/* CREAR LAS TABLAS EN LA BBDD */

CREATE TABLE BRAND (
    ID INT NOT NULL AUTO_INCREMENT, 
    NAME VARCHAR(50), 
    PRIMARY KEY(ID));

CREATE TABLE PRICES (
    ID INT NOT NULL AUTO_INCREMENT, 
    BRAND_ID INT, 
    START_DATE TIMESTAMP, 
    END_DATE TIMESTAMP, 
    PRICE_LIST INT, 
    PRODUCT_ID INT, 
    PRIORITY INT, 
    PRICE DOUBLE, 
    CURR VARCHAR(3),
    PRIMARY KEY(ID),
    FOREIGN KEY (BRAND_ID) REFERENCES BRAND(ID));

